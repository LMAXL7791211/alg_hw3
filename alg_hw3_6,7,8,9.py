print('6. В одномерном массиве найти сумму элементов, находящихся между минимальным и максимальным элементами.\n'
      ' Сами минимальный и максимальный элементы в сумму не включать.\n')

a = [1, -3, -3, -5, -2, 3, 1, 0, -3, -4, 6, -1, 5]
sum_el = 0

for el in a[min(a.index(max(a)), a.index(min(a))) + 1: max(a.index(max(a)), a.index(min(a)))]:
    sum_el += el

print(f'Массив:\n{a}\nСумма элементов минимальным и максимальным элементами в массиве = {sum_el}.\n\n')

