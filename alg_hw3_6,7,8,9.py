print('9. Найти максимальный элемент среди минимальных элементов столбцов матрицы.\n')

a = [
    [1, 2, 3, 4, 10],
    [4, 5, 6, 7, 22],
    [6, 7, 8, 9, 30],
    [9, 0, 2, 4, 15]
    ]

print('Матрица для поиска:')
for line in a:
    print(line)


rotated = tuple(zip(*a))
